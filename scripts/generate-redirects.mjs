#!/usr/bin/env node
/**
 * Reads the legacy redirects CSV exported from Webflow and emits a
 * normalized TypeScript map for Astro's `redirects` config to consume.
 *
 * Normalizations applied (so the output is clean for Astro):
 *   1. `/foo,https://www.allsafeit.com/bar` → `/foo: /bar` (strip self-host)
 *   2. `/foo,https://www.allsafeit.com`     → `/foo: /` (just the root)
 *   3. `/foo,/allsafeit.com`                → `/foo: /` (typo in source CSV)
 *   4. External destinations kept as-is (WP media → Webflow CDN, etc.)
 *   5. Duplicate sources reported and last-write-wins
 *   6. Self-referencing (`/foo` → `/foo`) skipped
 *
 * Run:
 *   node scripts/generate-redirects.mjs
 *
 * Inputs:
 *   ../allsafeit-webflow/allsafe-REDIRECTS.csv   (relative to project root)
 *
 * Outputs:
 *   src/data/redirects.ts
 */

import { readFileSync, writeFileSync, mkdirSync } from 'node:fs';
import { resolve, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const PROJECT_ROOT = resolve(__dirname, '..');
const CSV_PATH = resolve(PROJECT_ROOT, '../../allsafeit-webflow/allsafe-REDIRECTS.csv');
const OUT_PATH = resolve(PROJECT_ROOT, 'src/data/redirects.ts');
const SELF_HOST = 'https://www.allsafeit.com';

/**
 * Manual overrides — applied AFTER reading the CSV.
 *
 * The Webflow CSV is treated as a faithful copy of what's configured
 * in Webflow's redirects panel. When we need to correct a destination
 * (because the original target doesn't exist in the new Astro routing
 * tree, or the source CSV has a typo we can't fix upstream), the fix
 * lives HERE — not in the CSV.
 *
 * This way:
 *   - Re-exporting the CSV from Webflow doesn't wipe our corrections.
 *   - All deviations from Webflow are documented in one place.
 *   - Each override carries a short reason.
 */
const OVERRIDES = {
  // The legacy Webflow CSV pointed these at `/managed-it/it-helpdesk-support`,
  // a path that doesn't exist in the new Astro routing tree. Sending visitors
  // to the right sub-service detail page instead.
  '/2hours-free-it-support': '/services/managed-it/help-desk',
  '/hollywood-it-support-and-it-consulting-in-los-angeles': '/services/managed-it/help-desk',
  '/it-support-in-los-angeles': '/services/managed-it/help-desk',
};

const raw = readFileSync(CSV_PATH, 'utf-8');
const lines = raw.split(/\r?\n/).filter((l) => l.trim().length > 0);
const [, ...rows] = lines; // drop header

const map = new Map();
const issues = [];
let dupeCount = 0;
let selfRefCount = 0;

for (const row of rows) {
  // Naive CSV split — none of the entries contain commas inside values.
  const commaIdx = row.indexOf(',');
  if (commaIdx === -1) continue;
  const source = row.slice(0, commaIdx).trim();
  let target = row.slice(commaIdx + 1).trim();

  if (!source) continue;

  // Normalize the target.
  if (target === '/allsafeit.com' || target === 'allsafeit.com') {
    target = '/';
  } else if (target === SELF_HOST || target === `${SELF_HOST}/`) {
    target = '/';
  } else if (target.startsWith(`${SELF_HOST}/`)) {
    target = target.slice(SELF_HOST.length); // strip prefix → keeps leading /
  }

  // Ensure internal paths begin with /
  if (!target.startsWith('/') && !target.startsWith('http')) {
    target = `/${target}`;
  }

  // Skip self-references (would create infinite redirect loops).
  if (source === target) {
    selfRefCount++;
    issues.push(`SKIP self-ref: ${source}`);
    continue;
  }

  // Track duplicates.
  if (map.has(source)) {
    dupeCount++;
    issues.push(`DUPE source "${source}" → was "${map.get(source)}", now "${target}" (overwrite)`);
  }

  map.set(source, target);
}

// Apply manual overrides — corrects CSV destinations that point at
// non-existent paths in the Astro routing tree.
let overrideCount = 0;
for (const [src, tgt] of Object.entries(OVERRIDES)) {
  if (map.has(src) && map.get(src) !== tgt) {
    issues.push(`OVERRIDE ${src}: "${map.get(src)}" → "${tgt}"`);
    overrideCount++;
  }
  map.set(src, tgt);
}

// Sort so the output is stable across runs (alphabetical by source).
const sorted = [...map.entries()].sort(([a], [b]) => a.localeCompare(b));

// Generate the TS file.
const lines_out = [
  '// AUTO-GENERATED by scripts/generate-redirects.mjs',
  '// DO NOT EDIT BY HAND — re-run the script to regenerate.',
  '//',
  '// Source: ../allsafeit-webflow/allsafe-REDIRECTS.csv (exported from Webflow).',
  `// Generated: ${new Date().toISOString().split('T')[0]} (${sorted.length} entries).`,
  '//',
  '// Consumed by astro.config.mjs `redirects` option. Astro emits a 301 by',
  '// default for each entry. The Cloudflare Worker handles the redirect at',
  '// the edge — no extra Worker code required.',
  '',
  'export const LEGACY_REDIRECTS: Record<string, string> = {',
];

for (const [source, target] of sorted) {
  // Escape any quote inside the source/target (none expected, but safe).
  const safeSource = JSON.stringify(source);
  const safeTarget = JSON.stringify(target);
  lines_out.push(`  ${safeSource}: ${safeTarget},`);
}

lines_out.push('};');
lines_out.push('');

// Make sure the output dir exists.
mkdirSync(dirname(OUT_PATH), { recursive: true });
writeFileSync(OUT_PATH, lines_out.join('\n'), 'utf-8');

// Report.
console.log(`✓ Wrote ${sorted.length} redirects to ${OUT_PATH}`);
console.log(`  - Input rows:        ${rows.length}`);
console.log(`  - Duplicates merged: ${dupeCount}`);
console.log(`  - Self-refs skipped: ${selfRefCount}`);
console.log(`  - Manual overrides:  ${overrideCount}`);
if (issues.length > 0) {
  console.log('\nIssues:');
  for (const issue of issues) console.log(`  ${issue}`);
}
