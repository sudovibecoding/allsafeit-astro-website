/**
 * Post-build step: wrap Astro's Cloudflare Worker entry with a thin
 * redirect-first pre-handler.
 *
 * Why this exists:
 *   - Cloudflare Workers Static Assets caps `_redirects` at 100 dynamic
 *     rules; our 271 legacy URLs + 4 wildcards don't fit.
 *   - Cloudflare Bulk Redirects require a zone (production domain), so
 *     they can't serve the workers.dev staging URL.
 *   - Astro middleware in static mode doesn't fire for paths that don't
 *     match a route — and legacy URLs deliberately don't match any
 *     route, so middleware can't catch them.
 *
 * The wrapper:
 *   - Runs FIRST in the Worker fetch handler
 *   - Looks up the request path in a baked-in redirect map → 301 if hit
 *   - Tries 4 service-category prefix wildcards → 301 if hit
 *   - Otherwise delegates to Astro's original Worker (which handles
 *     /api/contact SSR + static asset fallback via env.ASSETS)
 *
 * Astro pages stay statically prerendered — this script doesn't change
 * Astro's output, it only adds a redirect-routing layer in front of the
 * Worker entry. Works identically on workers.dev, custom domains, and
 * preview URLs because path-based logic doesn't care about hostname.
 *
 * Runs after `astro build` as part of the npm build script.
 */
import { renameSync, writeFileSync, existsSync } from 'node:fs';
import { LEGACY_REDIRECTS } from '../src/data/redirects.ts';

const WORKER_DIR = 'dist/_worker.js';
const ORIGINAL_ENTRY = `${WORKER_DIR}/index.js`;
const ASTRO_ENTRY_RENAMED = `${WORKER_DIR}/astro-entry.mjs`;

if (!existsSync(ORIGINAL_ENTRY)) {
  console.error(`✗ Astro Worker entry not found at ${ORIGINAL_ENTRY} — did astro build run?`);
  process.exit(1);
}

// Move Astro's entry aside. We replace `index.js` (the wrangler entry
// point) with our wrapper that delegates back to the renamed file.
renameSync(ORIGINAL_ENTRY, ASTRO_ENTRY_RENAMED);

// Inline the redirect map. ~15 KB minified, well under Workers' 10 MB cap.
const LEGACY_JSON = JSON.stringify(LEGACY_REDIRECTS);

const wrapper = `// AUTO-GENERATED by scripts/build-worker-wrapper.mjs at build time.
// Edits will be overwritten on the next \`npm run build\`.
//
// Redirect-first pre-handler. NOT Astro SSR — pages stay statically
// prerendered. This wrapper exists solely to intercept legacy URLs at
// the edge before delegating to Astro's Worker entry.

import astroEntry from './astro-entry.mjs';

const LEGACY_REDIRECTS = ${LEGACY_JSON};

const SERVICE_WILDCARDS = [
  ['/managed-it/', '/services/managed-it/'],
  ['/cybersecurity/', '/services/cybersecurity/'],
  ['/it-consulting/', '/services/it-consulting/'],
  ['/it-infrastructure-and-cloud/', '/services/it-infrastructure-and-cloud/'],
];

export default {
  async fetch(request, env, ctx) {
    const url = new URL(request.url);
    const pathname = url.pathname;
    const search = url.search;

    // Exact-match legacy URL → 301.
    // Try \`pathname + search\` first so query-string-specific entries
    // (e.g. /?oxy_user_library=service-banner) match before bare paths.
    const exact = LEGACY_REDIRECTS[pathname + search] ?? LEGACY_REDIRECTS[pathname];
    if (exact) {
      const target = exact.startsWith('http') ? exact : new URL(exact, url.origin).href;
      return Response.redirect(target, 301);
    }

    // Service-category prefix wildcards. Catches anything Webflow had
    // at the bare path (e.g. /cybersecurity/foo → /services/cybersecurity/foo).
    for (const [from, to] of SERVICE_WILDCARDS) {
      if (pathname.startsWith(from)) {
        const rest = pathname.slice(from.length);
        return Response.redirect(new URL(to + rest + search, url.origin).href, 301);
      }
    }

    // No redirect — delegate to Astro's Worker (handles /api/contact SSR
    // + static asset fallback via env.ASSETS).
    return astroEntry.fetch(request, env, ctx);
  }
};

// Re-export anything Astro's entry exports (e.g. pageMap) in case other
// tooling expects it.
export { pageMap } from './astro-entry.mjs';
`;

writeFileSync(ORIGINAL_ENTRY, wrapper, 'utf8');

const entryCount = Object.keys(LEGACY_REDIRECTS).length;
console.log(`✓ Worker wrapped with ${entryCount} legacy + 4 wildcard redirects`);
